# Problema 1011 - Esfera @ Beecrowd
# Leitura e armazenamento do valor do raio da esfera.
r=float(input())
# Criação e definição do valor de π.
pi=3.14159
# Cálculo do volume de uma esfera e armazenamento do resultado.
v=(4/3)*pi*(r**3)
# Impressão do resultado armazenado na variável v com precisão de 3 casas decimais após o ponto.
print(f"VOLUME = {v:.3f}")
