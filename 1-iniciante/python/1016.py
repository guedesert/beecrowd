# Problema 1016 - Distância @ Beecrowd
# Lê e armazena a distância do carro Y em relação carro X
distancia = int(input())
# Calcula o tempo gasto considerando que cada km de distância demoram 2 minutos, como informado no enunciado da questão e como pode ser previsto pelas entradas e saídas informadas, mesmo considerando que a razão correta entre as velocidades do carro X e Y, na verdade, é de 1,5.
tempo = distancia*2
# Imprime o resutado do tempo em minutos.
print(f"{tempo} minutos")
