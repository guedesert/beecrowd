# Problema 1003 - Soma Simples @ Beecrowd
# Entrada dos 2 valores inteiros a serem somados, A e B.
A=int(input())
B=int(input())
# Atribuição da soma dos valores das variáveis A e B à variável SOMA.
SOMA=A+B
# Impressão do valor da variável SOMA.
print(f"SOMA = {SOMA}")
